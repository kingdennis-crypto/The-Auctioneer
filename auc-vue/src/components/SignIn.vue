<template>
  <section class="text-left mt-10">
    <div class="flex flex-col items-center justify-center px-6 py-8 mx-auto lg:py-0">
      <div class="w-full bg-white rounded-lg shadow-md md:mt-0 sm:max-w-md xl:p-0">
        <div class="p-6 space-y-4 md:space-y-6 sm:p-8">
          <h1 class="text-xl font-bold leading-tight tracking-tight text-gray-900 md:text-2xl">
            Sign in to your account
          </h1>
          <div class="space-y-4 md:space-y-6">
            <div>
              <label for="email" class="block mb-2 text-sm font-medium text-gray-900">Your email</label>
              <input v-model="this.email" type="email" name="email" id="email" class="bg-gray-50 border border-gray-300 text-gray-900 sm:text-sm rounded-lg focus:ring-primary-600 focus:border-primary-600 block w-full p-2.5 " placeholder="name@company.com" required="">
            </div>
            <div>
              <label for="password" class="block mb-2 text-sm font-medium text-gray-900">Password</label>
              <input v-model="this.password" type="password" name="password" id="password" placeholder="••••••••" class="bg-gray-50 border border-gray-300 text-gray-900 sm:text-sm rounded-lg focus:ring-primary-600 focus:border-primary-600 block w-full p-2.5" required="">
            </div>
            <div class="flex items-center justify-between">
              <div class="flex items-start">
                <div class="flex items-center h-5">
                  <input id="remember" aria-describedby="remember" type="checkbox" class="w-4 h-4 border border-gray-300 rounded bg-gray-50 focus:ring-3 focus:ring-primary-300" required="">
                </div>
                <div class="ml-3 text-sm">
                  <label for="remember" class="text-gray-500">Remember me</label>
                </div>
              </div>
              <a href="#" class="text-sm font-medium text-primary-600 hover:underline">Forgot password?</a>
            </div>
            <button @click="signIn" type="submit" class="w-full text-white bg-blue-600 hover:bg-blue-700 focus:ring-4 focus:outline-none focus:ring-primary-300 font-medium rounded-lg text-sm px-5 py-2.5 text-center">Sign in</button>
            <p class="text-sm font-light text-gray-500">
              Don't have an account yet? <a href="#" class="font-medium text-primary-600 hover:underline">Sign up</a>
            </p>
          </div>
        </div>
      </div>
    </div>
  </section>
</template>

<script>
export default {
  name: "SignIn",
  inject: ["sessionSbService"],
  data() {
    return {
      email: "john@hva.nl",
      password: "john",
    }
  },
  created() {
    const signOut = this.$route.query.signOut;

    if (signOut) {
      this.sessionSbService.signOut();

      this.$router.push('/sign-in');
    }
  },
  methods: {
    async signIn() {
      const isEmpty = this.email === "" || this.password === "";

      if (isEmpty) {
        // TODO Change this later
        return alert("BITCH YOU HAVENT ENTERED ALL STUFF");
      }

      const returnObj = await this.sessionSbService.asyncSignIn(this.email, this.password);

      if (returnObj.status === 401) {
        return alert("You've entered the wrong credentials");
      }

      this.$router.push('/');
    }
  }
}
</script>

